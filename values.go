package main

import "fmt"

func main() {

	// string concatenation with "+"

	fmt.Println("go" + "lang")

	// integer & float support

	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// typical boolean operators.

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
