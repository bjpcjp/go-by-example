/* variadic-functions.go
functions that can be called with varying numbers of arguments.
*/

package main

import "fmt"

// this example takes arbitrary number of integers

func sum(nums ...int) {
	fmt.Print(nums, " ")
	total := 0
	for _, num := range nums {
		total += num
	}
	fmt.Println(total)
}

func main() {

	sum(1, 2)
	sum(1, 2, 3)

	nums := []int{1, 2, 3, 4}
	sum(nums...)
}
